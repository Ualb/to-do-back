import {Entity, model, property} from '@loopback/repository';

@model({settings: {strict: false}})
export class Task extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  listID: string;

  @property({
    type: 'string',
    required: true,
  })
  title: string;

  @property({
    type: 'boolean',
    required: true,
  })
  isComplete: boolean;

  @property({
    type: 'date',
    required: true,
  })
  hourFinish: string;

  @property({
    type: 'boolean',
    required: true,
  })
  remember: boolean;

  // Define well-known properties here

  // Indexer property to allow additional data
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  [prop: string]: any;

  constructor(data?: Partial<Task>) {
    super(data);
  }
}

